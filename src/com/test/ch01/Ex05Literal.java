package com.test.ch01;

public class Ex05Literal {
	public static void main(String[] args) {
		int a = 7;
		System.out.println(7 + "은 정수 리터럴입니다.");
		System.out.println("정수 리터럴 " + 7 + "을 변수 a에 저장(대입)하였습니다.");
		System.out.println(3.14 + "는 실수 리터럴입니다.");
		System.out.println("변수에 저장되기 전의 숫자나 문자를 리터럴이라고 합니다.");
		
	}
}
